    Invoke-expression "& .\Master_Menu.PS1 -Credential $Cred"
    Start_Menu($select)
}

function Start_Menu{
    Write-Host '
    ************************************************************************************
    *                         Right Fax SandBox Select Configuration Menu              *        
    *                                                                                  *
    ************************************************************************************
    *          Collective IN1                *     Collective OUT3                     *
    *          Type 1                        *     Type 7                              *
    *                                        *                                         *
    *          Collective IN2                *     Collective OUT4                     *
    *          Type 2                        *     Type 8                              *
    *                                        *                                         *
    *          Collective IN3                *     Collective OUT5                     *
    *          Type 3                        *     Type 9                              *
    *                                        *                                         *
    *          Collective IN4                *     Collective OUT6                     *
    *          Type 4                        *     Type 10                             *
    *                                        *                                         *
    *          Collective OUT1               *     Collective FAX                      *
    *          Type 5                        *     Type 11 for FAX-A                   *
    *                                        *     Type 12 for FAX-B                   *
    *          Collective OUT2               *                                         *
    *          Type 6                        *   Type Main to Return to main Menu      *
    *                                        *                                         *
    *                                        *   Press X to Exit                       *
    *                                        *                                         *
    ************************************************************************************
    ' 
   
    
    $addorchange = Read-Host -Prompt 'Enter Choice'
    if ($addorchange -like "1"){IN1($addorchange)}
    if ($addorchange -like "2"){IN2($addorchange)}
    if ($addorchange -like "3"){IN3($addorchange)}
    if ($addorchange -like "4"){IN4($addorchange)}
    if ($addorchange -like "5"){OUT1($addorchange)}
    if ($addorchange -like "6"){OUT2($addorchange)} 
    if ($addorchange -like "7"){OUT3($addorchange)}
    if ($addorchange -like "8"){OUT4($addorchange)}
    if ($addorchange -like "9"){OUT5($addorchange)}
    if ($addorchange -like "10"){OUT6($addorchange)}
    if ($addorchange -like "11"){FAXA($addorchange)}
    if ($addorchange -like "12"){FAXB($addorchange)}
    if ($addorchange -like "Main"){Master_Menu($addorchange)}
    if ($addorchange -like "x"){BYEING($addorchange)}
    
    }
    
    Function BYEING{ 
    
    Write-Host "Exiting Right Fax System Install"
        Break
    }
    
    Start_Menu($select)
    